win.data <- list(vv = data$vv, centroide = data$centroide, n= nrow(data))

# Modelo
sink("GLM_normal_general.jags")
cat("
model {

  # Priors
  beta0 ~ dnorm(0, 1e-6)
  beta1 ~ dnorm(0, 1e-6)
  sigma ~ dunif(0, 10)
  tau <- 1 / (sigma * sigma)

  # Likelihood
  for (i in 1:n) {
    centroide[i] ~ dnorm(mu[i], tau)
    mu[i] <- beta0 + beta1 * vv[i]

  }
}
",fill = TRUE)
sink()

#valores iniciales
inits <- function() list(beta0 = runif(1, -2, 2), beta1 = runif(1, -3, 3))

